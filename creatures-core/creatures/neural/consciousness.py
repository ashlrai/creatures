"""Consciousness metrics for spiking neural networks.

Implements four complementary measures of neural consciousness/awareness:

1. **Integrated Information (Φ)** — Tononi's IIT: how much more information
   the whole system generates than the sum of its parts.
2. **Global Workspace Ignition** — Dehaene/Changeux: detection of sudden
   broadcast events where local activity propagates globally.
3. **Neural Complexity (CN)** — Tononi-Sporns-Edelman: integration of
   mutual information across multiple spatial scales.
4. **Perturbational Complexity Index (PCI)** — Casali et al.: compress
   the network's response to perturbation via Lempel-Ziv.

All metrics operate on spike trains (indices + times) and optionally
on connectome structure for graph-based measures.

References:
    - Tononi (2004) "An information integration theory of consciousness"
    - Dehaene & Changeux (2011) "Experimental and theoretical approaches
      to conscious processing"
    - Tononi, Sporns & Edelman (1994) "A measure for brain complexity"
    - Casali et al. (2013) "A theoretically based index of consciousness"
"""

from __future__ import annotations

import logging
from dataclasses import dataclass, field
from typing import Any

import numpy as np

logger = logging.getLogger(__name__)


# ======================================================================
# Shared helpers
# ======================================================================

def _build_spike_matrix(
    spike_indices: np.ndarray,
    spike_times_ms: np.ndarray,
    neuron_ids: np.ndarray,
    n_bins: int,
    bin_ms: float,
) -> np.ndarray:
    """Build a binary (n_bins, n_neurons) spike matrix from raw spike data.

    Args:
        spike_indices: Neuron index for each spike.
        spike_times_ms: Time (ms) for each spike.
        neuron_ids: Array of neuron IDs to include (column order).
        n_bins: Number of time bins.
        bin_ms: Width of each time bin in ms.

    Returns:
        Binary float32 matrix of shape (n_bins, len(neuron_ids)).
    """
    mat = np.zeros((n_bins, len(neuron_ids)), dtype=np.float32)
    remap = {nid: i for i, nid in enumerate(neuron_ids)}
    for idx, t in zip(spike_indices, spike_times_ms):
        if idx in remap:
            b = min(int(t / bin_ms), n_bins - 1)
            mat[b, remap[idx]] = 1.0
    return mat


# ======================================================================
# Data structures
# ======================================================================

@dataclass
class ConsciousnessReport:
    """Unified report from all consciousness metrics."""
    phi: float = 0.0
    phi_partition: dict[str, Any] = field(default_factory=dict)
    ignition_events: list[dict] = field(default_factory=list)
    ignition_rate: float = 0.0
    neural_complexity: float = 0.0
    complexity_profile: list[float] = field(default_factory=list)
    pci: float = 0.0
    pci_details: dict[str, Any] = field(default_factory=dict)

    def summary(self) -> str:
        return (
            f"Φ={self.phi:.4f} | Ignitions={len(self.ignition_events)} "
            f"({self.ignition_rate:.1f}/s) | CN={self.neural_complexity:.4f} | "
            f"PCI={self.pci:.4f}"
        )


# ======================================================================
# 1. Integrated Information (Φ) — IIT
# ======================================================================

def compute_phi(
    spike_indices: np.ndarray,
    spike_times_ms: np.ndarray,
    n_neurons: int,
    duration_ms: float,
    bin_ms: float = 10.0,
    top_k: int | None = None,
    n_partitions: int = 50,
    seed: int = 42,
) -> dict[str, Any]:
    """Compute Integrated Information (Φ) via greedy partition search.

    Φ measures how much information is generated by the whole system
    above and beyond what is generated by its parts independently.

    Φ = MI(past → future | whole) - max_partition Σ MI(past → future | part_i)

    Since exact Φ is exponential in neuron count, we use:
    1. Select top-k most active neurons
    2. Bin spikes into discrete time windows
    3. Compute time-lagged mutual information for the whole system
    4. Search partitions greedily to find the minimum information partition
    5. Φ = whole MI - partitioned MI

    Args:
        spike_indices: Neuron index for each spike.
        spike_times_ms: Time (ms) for each spike.
        n_neurons: Total neuron count.
        duration_ms: Total recording duration.
        bin_ms: Time bin width for discretization.
        top_k: Number of most active neurons to analyze. If None, scales
            adaptively with network size: min(max(30, int(sqrt(n)*2)), 100).
        n_partitions: Number of random partitions to try.
        seed: Random seed.

    Returns:
        Dictionary with phi, partition info, and per-neuron contributions.
    """
    # Adaptive top_k: scale with network size for better coverage
    if top_k is None:
        top_k = min(max(30, int(np.sqrt(n_neurons) * 2)), 100)
    if len(spike_indices) == 0:
        return {"phi": 0.0, "partition": {}, "n_analyzed": 0}

    rng = np.random.default_rng(seed)
    spike_indices = np.asarray(spike_indices)
    spike_times_ms = np.asarray(spike_times_ms)

    # Select neurons with diverse firing patterns (not just the most active)
    unique, counts = np.unique(spike_indices, return_counts=True)

    # Filter to neurons with enough spikes to be informative
    min_spikes = max(3, int(duration_ms / bin_ms * 0.05))
    active_mask = counts >= min_spikes
    active_neurons = unique[active_mask]
    active_counts = counts[active_mask]

    if len(active_neurons) < 4:
        return {"phi": 0.0, "partition": {}, "n_analyzed": len(active_neurons)}

    # Build spike matrix for ALL active neurons first, then select diverse ones
    n_bins = max(int(duration_ms / bin_ms), 2)
    candidate_k = min(len(active_neurons), top_k * 10)
    # Take a spread of activity levels: top, middle, lower
    if len(active_neurons) > candidate_k:
        sorted_idx = np.argsort(-active_counts)
        # Sample from top third, middle third, bottom third
        third = candidate_k // 3
        top_neurons = sorted_idx[:third]
        mid_start = len(sorted_idx) // 3
        mid_neurons = sorted_idx[mid_start:mid_start + third]
        low_start = 2 * len(sorted_idx) // 3
        low_neurons = sorted_idx[low_start:low_start + (candidate_k - 2 * third)]
        candidate_idx = np.concatenate([top_neurons, mid_neurons, low_neurons])
        candidates = active_neurons[candidate_idx]
    else:
        candidates = active_neurons

    # Build spike matrix for candidates
    spike_mat_full = _build_spike_matrix(
        spike_indices, spike_times_ms, candidates, n_bins, bin_ms,
    )

    # Select top_k neurons with most diverse patterns (highest entropy)
    entropies = np.zeros(len(candidates))
    for j in range(len(candidates)):
        col = spike_mat_full[:, j]
        p = col.mean()
        if 0 < p < 1:
            entropies[j] = -p * np.log2(p) - (1 - p) * np.log2(1 - p)

    # Pick neurons with highest individual entropy (most variable firing)
    if len(candidates) > top_k:
        diverse_idx = np.argsort(-entropies)[:top_k]
    else:
        diverse_idx = np.arange(len(candidates))
        top_k = len(candidates)

    selected = candidates[diverse_idx]
    spike_matrix = spike_mat_full[:, diverse_idx]

    # Use Gaussian approximation of MI via covariance (practical IIT)
    # This captures temporal correlations due to synaptic connections.
    # MI_gauss = 0.5 * log(det(cov_past) * det(cov_future) / det(cov_joint))

    past = spike_matrix[:-1]  # (n_bins-1, k)
    future = spike_matrix[1:]  # (n_bins-1, k)

    whole_mi = _gaussian_mi(past, future)

    # Greedy partition search for minimum information partition
    best_partition_mi = whole_mi
    best_partition = None

    for _ in range(n_partitions):
        perm = rng.permutation(top_k)
        split = max(2, rng.integers(top_k // 4, 3 * top_k // 4 + 1))
        part_a = perm[:split]
        part_b = perm[split:]

        mi_a = _gaussian_mi(past[:, part_a], future[:, part_a])
        mi_b = _gaussian_mi(past[:, part_b], future[:, part_b])
        partition_mi = mi_a + mi_b

        if partition_mi < best_partition_mi:
            best_partition_mi = partition_mi
            best_partition = {
                "a": part_a.tolist(),
                "b": part_b.tolist(),
                "mi_a": float(mi_a),
                "mi_b": float(mi_b),
            }

    phi = max(0.0, whole_mi - best_partition_mi)

    return {
        "phi": float(phi),
        "whole_mi": float(whole_mi),
        "partition_mi": float(best_partition_mi),
        "partition": best_partition or {},
        "n_analyzed": top_k,
        "selected_neurons": selected.tolist(),
    }


def _gaussian_mi(x: np.ndarray, y: np.ndarray) -> float:
    """Compute mutual information under Gaussian approximation.

    MI_gauss = 0.5 * [log det(Σ_x) + log det(Σ_y) - log det(Σ_xy)]

    This is the standard practical approximation for IIT and captures
    temporal correlations between neural populations.
    """
    n_samples, n_x = x.shape
    _, n_y = y.shape
    if n_samples < 10 or n_x == 0 or n_y == 0:
        return 0.0

    # Add small regularization to prevent singular covariance
    eps = 1e-6

    # Covariance of x
    cov_x = np.cov(x.T) + eps * np.eye(n_x)
    # Covariance of y
    cov_y = np.cov(y.T) + eps * np.eye(n_y)
    # Joint covariance
    xy = np.hstack([x, y])
    cov_xy = np.cov(xy.T) + eps * np.eye(n_x + n_y)

    # Handle scalar covariance (1 neuron)
    if n_x == 1:
        cov_x = cov_x.reshape(1, 1)
    if n_y == 1:
        cov_y = cov_y.reshape(1, 1)

    # log det via slogdet for numerical stability
    _, logdet_x = np.linalg.slogdet(cov_x)
    _, logdet_y = np.linalg.slogdet(cov_y)
    _, logdet_xy = np.linalg.slogdet(cov_xy)

    mi = 0.5 * (logdet_x + logdet_y - logdet_xy)
    return max(0.0, float(mi))


def _time_lagged_mi(past: np.ndarray, future: np.ndarray, n_bins: int = 8) -> float:
    """Compute time-lagged mutual information between past and future states.

    Discretizes the multi-dimensional state by hashing binary patterns
    into integer labels, then computes standard MI.

    I(past; future) = H(past) + H(future) - H(past, future)
    """
    n_samples, n_vars = past.shape
    if n_samples < 10 or n_vars == 0:
        return 0.0

    # Hash each timestep's binary pattern into an integer label
    # For efficiency, use dot product with powers of 2
    if n_vars <= 20:
        powers = 2 ** np.arange(n_vars)
        past_labels = past.astype(int) @ powers
        future_labels = future.astype(int) @ powers
    else:
        # For larger systems, use coarser discretization
        # Sum active neurons in groups of 5
        group_size = 5
        n_groups = (n_vars + group_size - 1) // group_size
        past_grouped = np.zeros((n_samples, n_groups), dtype=int)
        future_grouped = np.zeros((n_samples, n_groups), dtype=int)
        for g in range(n_groups):
            s = g * group_size
            e = min(s + group_size, n_vars)
            past_grouped[:, g] = np.clip(past[:, s:e].sum(axis=1), 0, n_bins - 1).astype(int)
            future_grouped[:, g] = np.clip(future[:, s:e].sum(axis=1), 0, n_bins - 1).astype(int)
        powers = n_bins ** np.arange(n_groups)
        past_labels = past_grouped @ powers
        future_labels = future_grouped @ powers

    return _mi_from_labels(past_labels, future_labels)


def _mi_from_labels(x: np.ndarray, y: np.ndarray) -> float:
    """Mutual information between two integer label arrays."""
    n = len(x)
    if n < 2:
        return 0.0

    # Joint histogram via numpy
    xy = np.stack([x, y], axis=1)
    # Use structured array for unique counting
    xy_tuples = np.ascontiguousarray(xy).view(
        np.dtype((np.void, xy.dtype.itemsize * xy.shape[1]))
    )
    _, joint_counts = np.unique(xy_tuples, return_counts=True)
    _, x_counts = np.unique(x, return_counts=True)
    _, y_counts = np.unique(y, return_counts=True)

    # Entropy computation
    def entropy(counts):
        p = counts / counts.sum()
        p = p[p > 0]
        return -np.sum(p * np.log2(p))

    h_x = entropy(x_counts)
    h_y = entropy(y_counts)
    h_xy = entropy(joint_counts)

    return max(0.0, h_x + h_y - h_xy)


