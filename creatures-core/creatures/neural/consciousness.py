# 3. Neural Complexity (Tononi-Sporns-Edelman)
# ======================================================================

def compute_neural_complexity(
    spike_indices: np.ndarray,
    spike_times_ms: np.ndarray,
    n_neurons: int,
    duration_ms: float,
    bin_ms: float = 10.0,
    max_scale: int = 10,
    top_k: int | None = None,
    n_samples_per_scale: int = 20,
    seed: int = 42,
) -> dict[str, Any]:
    """Compute Neural Complexity (CN) across spatial scales.

    CN measures the degree to which a neural system simultaneously shows
    functional specialization (different parts do different things) and
    functional integration (parts coordinate).

    CN = Σ_k [MI(subset_k; rest) averaged over random subsets of size k]

    High CN indicates a system between two extremes:
    - Fully random (no integration → low CN)
    - Fully synchronized (no specialization → low CN)

    Args:
        spike_indices, spike_times_ms: Spike data.
        n_neurons: Total neuron count.
        duration_ms: Recording duration.
        bin_ms: Discretization bin width.
        max_scale: Maximum subset size to test.
        top_k: Number of most active neurons to analyze.
        n_samples_per_scale: Random subsets per scale.
        seed: Random seed.

    Returns:
        Dictionary with complexity score and scale profile.
    """
    if len(spike_indices) == 0:
        return {"complexity": 0.0, "profile": [], "n_analyzed": 0}

    # Adaptive top_k: scale with network size
    if top_k is None:
        top_k = min(max(60, int(np.sqrt(n_neurons) * 3)), 150)

    rng = np.random.default_rng(seed)
    spike_indices = np.asarray(spike_indices)
    spike_times_ms = np.asarray(spike_times_ms)

    # Select most active neurons
    unique, counts = np.unique(spike_indices, return_counts=True)
    if len(unique) > top_k:
        selected = unique[np.argsort(-counts)[:top_k]]
    else:
        selected = unique
        top_k = len(selected)

    if top_k < 4:
        return {"complexity": 0.0, "profile": [], "n_analyzed": top_k}

    # Build binary spike matrix
    n_bins = max(int(duration_ms / bin_ms), 2)
    spike_matrix = _build_spike_matrix(
        spike_indices, spike_times_ms, selected, n_bins, bin_ms,
    )

    max_scale = min(max_scale, top_k // 2)
    profile = []

    for k in range(1, max_scale + 1):
        mi_values = []
        for _ in range(n_samples_per_scale):
            subset = rng.choice(top_k, size=k, replace=False)
            rest = np.setdiff1d(np.arange(top_k), subset)
            if len(rest) == 0:
                continue

            # Compute MI between subset and rest
            sub_data = spike_matrix[:, subset]
            rest_data = spike_matrix[:, rest]

            # Hash each to labels
            if k <= 20:
                sub_labels = sub_data.astype(int) @ (2 ** np.arange(k))
            else:
                sub_labels = sub_data.sum(axis=1).astype(int)

            rest_k = min(len(rest), 20)
            if rest_k <= 20:
                rest_labels = rest_data[:, :rest_k].astype(int) @ (2 ** np.arange(rest_k))
            else:
                rest_labels = rest_data.sum(axis=1).astype(int)

            mi = _mi_from_labels(sub_labels, rest_labels)
            mi_values.append(mi)

        avg_mi = float(np.mean(mi_values)) if mi_values else 0.0
        profile.append(avg_mi)

    complexity = float(np.sum(profile))

    return {
        "complexity": complexity,
        "profile": profile,
        "max_scale": max_scale,
        "n_analyzed": top_k,
    }


# ======================================================================
# 4. Perturbational Complexity Index (PCI)
# ======================================================================

def compute_pci(
    response_matrix: np.ndarray,
    threshold: float | None = None,
) -> dict[str, Any]:
    """Compute Perturbational Complexity Index from an evoked response.

    PCI = LZ(response) / H(response)

    where LZ is the Lempel-Ziv complexity of the binarized spatiotemporal
    response matrix, and H is its source entropy.

    Higher PCI indicates more complex (conscious-like) processing:
    - Simple propagation (chain) → low PCI
    - No propagation (disconnected) → low PCI
    - Rich, differentiated propagation → high PCI

    Args:
        response_matrix: Binary (n_time, n_neurons) evoked response matrix.
            1 = neuron fired in this time bin, 0 = silent.
        threshold: Binarization threshold (default: auto from data).

    Returns:
        Dictionary with PCI score and component metrics.
    """
    mat = np.asarray(response_matrix, dtype=np.float32)
    if mat.size == 0:
        return {"pci": 0.0, "lz_complexity": 0, "source_entropy": 0.0}

    n_time, n_neurons = mat.shape

    # Binarize — use the matrix values directly if already binary (0/1),
    # otherwise threshold at the median of nonzero values
    if threshold is None:
        nonzero = mat[mat > 0]
        if len(nonzero) > 0:
            threshold = np.median(nonzero) * 0.5
        else:
            threshold = 0.5
    binary = (mat >= threshold).astype(np.uint8)

    # Flatten spatiotemporal matrix to 1D bit string (column-major for spatial spread)
    bit_string = binary.T.ravel()

    # Lempel-Ziv complexity
    lz = _lempel_ziv_complexity(bit_string)

    # Source entropy
    p1 = np.mean(binary)
    if p1 <= 0 or p1 >= 1:
        source_entropy = 0.0
    else:
        source_entropy = -p1 * np.log2(p1) - (1 - p1) * np.log2(1 - p1)

    # Normalised PCI
    n_total = n_time * n_neurons
    if source_entropy > 0 and n_total > 0:
        # Normalise by the expected LZ for a random binary string with same density
        expected_lz = n_total * source_entropy / np.log2(n_total + 1)
        pci = lz / max(expected_lz, 1e-6)
    else:
        pci = 0.0

    return {
        "pci": float(pci),
        "lz_complexity": int(lz),
        "source_entropy": float(source_entropy),
        "n_time": n_time,
        "n_neurons": n_neurons,
        "active_fraction": float(p1),
    }


def _lempel_ziv_complexity(s: np.ndarray) -> int:
    """Compute Lempel-Ziv complexity of a binary sequence (LZ76).

    Uses set-based substring lookup for O(n log n) average performance
    instead of O(n²k) brute-force scanning.
    """
    n = len(s)
    if n == 0:
        return 0

    # Convert to bytes for fast substring matching
    s_bytes = bytes(s.astype(np.uint8))
    seen: set[bytes] = set()
    complexity = 0
    i = 0

    while i < n:
        # Find the shortest substring starting at i not yet seen
        k = 1
        while i + k <= n:
            substr = s_bytes[i:i + k]
            if substr not in seen:
                seen.add(substr)
                complexity += 1
                i += k
                break
            k += 1
        else:
            # Reached end of string
            complexity += 1
            break

    return complexity


# ======================================================================
# Convenience: Run all metrics at once
# ======================================================================

def compute_all_consciousness_metrics(
    spike_indices: np.ndarray,
    spike_times_ms: np.ndarray,
    n_neurons: int,
    duration_ms: float,
    response_matrix: np.ndarray | None = None,
    bin_ms: float = 10.0,
    top_k: int | None = None,
    seed: int = 42,
) -> ConsciousnessReport:
    """Compute all four consciousness metrics.

    Args:
        spike_indices, spike_times_ms: Spike data from simulation.
        n_neurons: Total neuron count.
        duration_ms: Total recording duration.
        response_matrix: Optional evoked response matrix for PCI.
            If None, PCI is computed from the spike data directly.
        bin_ms: Time bin width for discretization.
        top_k: Number of neurons to analyze for Φ. If None, scales
            adaptively with network size.
        seed: Random seed.

    Returns:
        ConsciousnessReport with all metrics.
    """
    spike_indices = np.asarray(spike_indices)
    spike_times_ms = np.asarray(spike_times_ms)

    # Resolve adaptive top_k once for consistent usage across all metrics
    if top_k is None:
        top_k = min(max(30, int(np.sqrt(n_neurons) * 2)), 100)

    # 1. Integrated Information (Φ)
    phi_result = compute_phi(
        spike_indices, spike_times_ms, n_neurons, duration_ms,
        bin_ms=bin_ms, top_k=top_k, seed=seed,
    )

    # 2. Global Workspace Ignition
    ignition_events = detect_ignition_events(
        spike_indices, spike_times_ms, n_neurons,
    )
    ignition_rate = len(ignition_events) / max(duration_ms / 1000.0, 0.001)

    # 3. Neural Complexity
    complexity_result = compute_neural_complexity(
        spike_indices, spike_times_ms, n_neurons, duration_ms,
        bin_ms=bin_ms, top_k=min(top_k * 2, 60), seed=seed,
    )

    # 4. PCI
    if response_matrix is not None:
        pci_result = compute_pci(response_matrix)
    else:
        # Build response matrix from spikes
        n_bins = max(int(duration_ms / bin_ms), 2)
        n_k = min(n_neurons, 200)
        unique, counts = np.unique(spike_indices, return_counts=True)
        if len(unique) > n_k:
            selected = unique[np.argsort(-counts)[:n_k]]
        else:
            selected = unique
            n_k = len(selected)

        mat = _build_spike_matrix(
            spike_indices, spike_times_ms, selected, n_bins, bin_ms,
        )

        pci_result = compute_pci(mat)

    return ConsciousnessReport(
        phi=phi_result["phi"],
        phi_partition=phi_result,
        ignition_events=ignition_events,
        ignition_rate=ignition_rate,
        neural_complexity=complexity_result["complexity"],
        complexity_profile=complexity_result["profile"],
        pci=pci_result["pci"],
        pci_details=pci_result,
    )
