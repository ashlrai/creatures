# 2. Global Workspace Ignition Detection
# ======================================================================

def detect_ignition_events(
    spike_indices: np.ndarray,
    spike_times_ms: np.ndarray,
    n_neurons: int,
    activation_threshold: float = 0.10,
    window_ms: float = 50.0,
    min_gap_ms: float = 100.0,
    baseline_rate: float | None = None,
) -> list[dict[str, Any]]:
    """Detect Global Workspace-style ignition events.

    An "ignition" occurs when neural activity suddenly transitions from
    sparse/local to widespread/global activation. This is detected as a
    rapid increase in the fraction of active neurons within a time window.

    Args:
        spike_indices: Neuron index for each spike.
        spike_times_ms: Time (ms) for each spike.
        n_neurons: Total neuron count.
        activation_threshold: Fraction of neurons that must be active
            for an ignition (default 10%).
        window_ms: Time window for measuring activation spread.
        min_gap_ms: Minimum gap between ignition events.
        baseline_rate: Expected baseline activation rate. If None,
            computed from data.

    Returns:
        List of ignition event dicts with time, strength, participants.
    """
    if len(spike_indices) == 0 or n_neurons == 0:
        return []

    spike_indices = np.asarray(spike_indices)
    spike_times_ms = np.asarray(spike_times_ms)

    t_min = spike_times_ms.min()
    t_max = spike_times_ms.max()
    if t_max - t_min < window_ms:
        return []

    # Slide window across time
    step_ms = window_ms / 2  # 50% overlap
    window_starts = np.arange(t_min, t_max - window_ms, step_ms)

    # Compute activation fraction for each window
    activations = np.zeros(len(window_starts))
    for i, t_start in enumerate(window_starts):
        mask = (spike_times_ms >= t_start) & (spike_times_ms < t_start + window_ms)
        n_active = len(np.unique(spike_indices[mask]))
        activations[i] = n_active / n_neurons

    if baseline_rate is None:
        baseline_rate = np.median(activations)

    # Detect threshold crossings (activation above threshold AND above baseline)
    threshold = max(activation_threshold, baseline_rate * 3.0)
    above = activations >= threshold

    # Find onset of each ignition event (transition from below to above)
    events = []
    last_event_time = -min_gap_ms * 2

    for i in range(1, len(above)):
        if above[i] and not above[i - 1]:
            event_time = window_starts[i]
            if event_time - last_event_time < min_gap_ms:
                continue

            # Find peak activation within this event
            peak_idx = i
            j = i + 1
            while j < len(above) and above[j]:
                if activations[j] > activations[peak_idx]:
                    peak_idx = j
                j += 1

            # Get participating neurons
            peak_start = window_starts[peak_idx]
            mask = (spike_times_ms >= peak_start) & (
                spike_times_ms < peak_start + window_ms
            )
            participants = np.unique(spike_indices[mask])

            events.append({
                "time_ms": float(event_time),
                "peak_time_ms": float(window_starts[peak_idx]),
                "peak_activation": float(activations[peak_idx]),
                "n_participating": int(len(participants)),
                "fraction_participating": float(len(participants) / n_neurons),
                "duration_ms": float((j - i) * step_ms),
                "strength": float(activations[peak_idx] / max(baseline_rate, 1e-6)),
            })
            last_event_time = event_time

    return events


